"""第六章 总结与下一步工作计划 — LLM Cloud-Native Distributed Inference Engine."""

from report.styles import add_heading1, add_heading2, add_body_text, add_table, add_page_break


def add_chapter6(doc):
    """Add Chapter 6: 总结与下一步工作计划."""

    add_page_break(doc)
    add_heading1(doc, "第六章 总结与下一步工作计划")

    # ── 6.1 已完成工作总结 ────────────────────────────────────────────────
    add_heading2(doc, "6.1 已完成工作总结")

    add_body_text(doc,
        "本文设计并实现了一个基于微服务架构的分布式LLM推理调度系统（llm-scheduler）。"
        "截至目前，系统已完成以下核心工作：")

    add_body_text(doc,
        "（1）完成了微服务架构的分布式LLM推理调度系统设计与实现。系统划分为Gateway网关层、"
        "Scheduler调度器、Monitoring监控服务、vLLM推理节点四个核心微服务，各服务通过RESTful API协同工作，"
        "实现了推理请求的统一接入、智能调度、负载均衡与响应回传的完整链路。")

    add_body_text(doc,
        "（2）实现了三种负载均衡策略及运行时热切换机制。系统支持Round Robin（轮询）、"
        "Least Connections（最少连接）和Weighted（加权）三种调度策略，"
        "可通过调度器API在运行时动态切换，无需重启任何服务组件，满足不同业务场景的调度需求。")

    add_body_text(doc,
        "（3）构建了完整的监控告警体系。监控服务实时采集各推理节点的心跳状态、活跃连接数、"
        "GPU利用率等关键指标，提供可视化仪表盘展示，并支持异常状态的自动告警通知。")

    add_body_text(doc,
        "（4）实现了基于心跳的节点健康管理和自动故障转移。推理节点定期向调度器发送心跳信号，"
        "调度器据此维护节点健康状态。当节点故障时，系统自动将请求路由至健康节点，"
        "节点恢复后自动重新注册，实现了较高的服务可用性。")

    add_body_text(doc,
        "（5）完成了Docker容器化部署方案。各微服务均通过Docker容器化封装，"
        "支持通过docker-compose一键启动完整系统，降低了部署与运维的复杂度，"
        "为后续的弹性伸缩和集群管理奠定了基础。")

    # ── 6.2 下一步工作计划 ────────────────────────────────────────────────
    add_heading2(doc, "6.2 下一步工作计划")

    add_body_text(doc,
        "在已完成的系统基础之上，后续将从推理能力增强、调度智能化、运维可观测性、资源管理及性能优化五个维度展开深入工作。")

    add_body_text(doc,
        "（1）流式推理支持。当前系统仅支持同步请求-响应模式的推理调用，后续将引入SSE（Server-Sent Events）"
        "流式传输机制，实现逐Token输出，大幅改善用户交互体验，使其与主流LLM服务接口保持一致。")

    add_body_text(doc,
        "（2）GPU感知调度。当前调度策略主要基于连接数和权重进行决策，"
        "后续将集成GPU利用率、显存使用率、KV Cache占用率等硬件感知指标，"
        "实现更精细化的智能调度，提升GPU资源的整体利用率。")

    add_body_text(doc,
        "（3）弹性伸缩。基于监控采集的负载指标，实现推理节点的自动扩缩容能力，"
        "在请求高峰期自动增加推理节点实例，在低谷期自动回收闲置资源，降低运营成本。")

    add_body_text(doc,
        "（4）分布式追踪。集成OpenTelemetry框架，实现从网关接收到推理返回的全链路请求追踪，"
        "支持跨服务的延迟分析与瓶颈定位，为性能优化提供数据支撑。")

    add_body_text(doc,
        "（5）多租户资源隔离。引入租户管理机制，支持不同用户组的资源配额、优先级与访问控制策略，"
        "满足多组织共享推理集群时的资源隔离与公平调度需求。")

    add_body_text(doc,
        "（6）性能优化。从连接池复用、批处理转发、模型预热等方面持续优化系统性能，"
        "降低首Token延迟，提高并发吞吐量，进一步提升系统的工程质量和生产就绪能力。")

    # ── 6.3 进度安排 ─────────────────────────────────────────────────────
    add_heading2(doc, "6.3 进度安排")

